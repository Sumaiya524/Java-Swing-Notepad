    private void jMenuItem15ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem15ActionPerformed
        // TODO add your handling code here:
        Font currentFont = output1.getFont();

    // Show a simple font selection dialog (system fonts)
        String[] fonts = GraphicsEnvironment.getLocalGraphicsEnvironment().getAvailableFontFamilyNames();
        String fontName = (String) JOptionPane.showInputDialog(
                this,
                "Select Font:",
                "Font Selection",
                JOptionPane.PLAIN_MESSAGE,
                null,
                fonts,
                currentFont.getFamily());

        if (fontName != null) {
            int style = currentFont.getStyle();
            int size = currentFont.getSize();
            Font newFont = new Font(fontName, style, size);
            output1.setFont(newFont);
        }
        
    }//GEN-LAST:event_jMenuItem15ActionPerformed

