    void openfile() {

    JFileChooser x = new JFileChooser();
    int a = x.showOpenDialog(null);

    if (a == 0) {
        String fname;
        fname = x.getSelectedFile().getPath();

        try {
            FileReader f = new FileReader(fname);
            Scanner s = new Scanner(f);

            while (s.hasNext() == true) {
                String msg = s.nextLine() + "\n\r";
                output1.append(msg);
            }

            s.close();
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
}
    
